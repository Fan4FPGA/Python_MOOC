#installer 库说明
#pip 

#pyinstaller -F <文件名.py>
#常用参数
#-h 查看帮助
#--clean 清理打包过程中的临时文件
#-D ,-onedir 默认值，生成dist文件夹 不建议使用，建议使用-F参数
#-F ，-onefile 只生成一个可执行文件
#-i <图标文件名.ico>  指定打包程序使用的图标


